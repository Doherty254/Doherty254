// switch statement

let color = 'RED'

switch(color.toLocaleLowerCase()) {
    case 'red':
        console.log('STOP')
        break
    case 'yellow':
        console.log('GET READY')
        break
    case 'green':
        console.log('GO')
        break
    default:
        console.log('ERROR! INVALID OUTPUT')
        break
}